#include <libft.h>

char	ft_convert_bit_to_char(char *s)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = 128;
	while (j > 0)
	{
		c += (s[i] - 48) * j;
		j /= 2;
		i++;
	}
	return (c);
}
