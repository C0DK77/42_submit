#include "libft.h"

int	ft_strisquote(char *s)
{
	size_t	len;

	len = ft_strlen(s);
	if ((s[0] == '\'' && s[len - 1] == '\''))
		return (1);
	if (s[0] == '"' && s[len - 1] == '"')
		return (1);
	return (0);
}
