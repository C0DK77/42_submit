#include "libft.h"

int	ft_strisquote(char *s)
{
	size_t	len;

	len = ft_strlen(s);
	return ((s[0] == '\'' && s[len - 1] == '\'') || (s[0] == '"' && s[len
			- 1] == '"'));
}
